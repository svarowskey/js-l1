/**
 * Функция производит необходимую математическую операцию с двумя числами.
 * Сложение - sum; Вычитание - subtraction; Умножение - multiplication; Деление - division.
 * @param {number} arg1
 * @param {number} arg2
 * @param {string} operation
 */
function mathOperation(arg1, arg2, operation) {
    switch (operation) {
        case "sum":
            sum(arg1, arg2);
            break;
        case "subtraction":
            subtraction(arg1, arg2);
            break;
        case "multiplication":
            multiplication(arg1, arg2);
            break;
        case "division":
            division(arg1, arg2);
            break;
    }
}

